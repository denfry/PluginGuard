/**
 * Russian translations of every security finding the Java analyzer can emit, keyed by `ruleId`.
 *
 * The backend produces English `title` / `description` / `recommendation` for each finding (see the
 * analyzers under `analyzer/src/main/java/dev/pluginguard/engine/`). This module mirrors that prose in
 * Russian so the UI can localise findings at runtime while still rendering the structured fields
 * (severity, category, evidence, location) through `web/lib/i18n.tsx`.
 *
 * Translation notes:
 *  - Code identifiers, API/method names, file names, URLs and ruleIds are kept verbatim.
 *  - Many English descriptions splice a concrete value (a class name, IP, permission, count) into the
 *    sentence. Because that exact value is also shown in the finding's separate `evidence` / `location`
 *    fields, the Russian text is phrased slightly more generically so it never has to re-insert it.
 *  - RU strings use template literals so apostrophes and quotes need no escaping.
 */

export interface LocalizableFinding {
  ruleId: string;
  title: string;
  description: string;
  recommendation: string;
}

